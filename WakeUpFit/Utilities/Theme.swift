import SwiftUI

struct Theme {
    /// Main buttons, progress rings, and active states. (#E08E6E)
    static let primary = Color("ThemePrimary")
    
    /// Badges, achievements, and secondary highlights. (#FFD573)
    static let secondary = Color("ThemeSecondary")
    
    /// Main app background; softer on the eyes than pure white. (#F9F3EA)
    static let background = Color("ThemeBackground")
    
    /// Card backgrounds and navigation bars. (#F2E6D8)
    static let surface = Color("ThemeSurface")
    
    /// Primary typography and heavy iconography. (#35475A)
    static let textDark = Color("ThemeTextDark")
    
    /// Deep accents for borders or subtle UI depth. (#B8860B)
    static let accent = Color("ThemeAccent")
}
